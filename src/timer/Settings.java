    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
  
if((buttonGroup1.getSelection()!=null)&&(!(jTextField1.getText().isEmpty()&& jTextField2.getText().isEmpty()))){
    try{
        f = new JFrame();
        f.setSize(300,300);
        tf = new JTextField();
        f.add(tf);
        f.setVisible(true);
        strDelay = (String) jComboBox2.getSelectedItem();
        delay = 1000*(Integer.valueOf(strDelay));

        if(buttonGroup1.getSelection().equals(jRadioButton1.getModel()))
        {    
            time = jTextField1.getText();
            h = Integer.valueOf(time.substring(0, 2));
            m = Integer.valueOf(time.substring(3, 5));
            s = Integer.valueOf(time.substring(6, 8));
            h2 = LocalTime.now().getHour();
            m2 = LocalTime.now().getMinute();
            s2 = LocalTime.now().getSecond();
            initialDelay = ((h*3600+m*60+s)-(h2*3600+m2*60+s2));
            if(initialDelay<0) initialDelay+=(3600*24); //IT WILL START NEXT DAY
        }      
             else if(buttonGroup1.getSelection().equals(jRadioButton2.getModel())) 
                 initialDelay = (1000*Integer.parseInt(jTextField2.getText()));

        t = new Timer(delay, new ActionListener() {
            public void actionPerformed(ActionEvent e) {
               if(!tf.getBackground().equals(c))tf.setBackground(c);
                else tf.setBackground(Color.WHITE); 
            }
        });            
        t.setInitialDelay(initialDelay);
        t.start();
    }catch(NumberFormatException ex) {System.out.println("NUMBER FORMAT EXCEPTION!");}
}
    }//GEN-LAST:event_jButton2ActionPerformed

